asio::ip::address to_address(const std::string& s)
{
    asio::error_code ec;
    return asio::ip::make_address(s, ec);
}

////////////////////////////////////////////////////////////////////////////////
std::uint16_t to_port(const std::string& s)
{
    char* end;
    auto ul = std::strtoul(s.data(), &end, 0);

    std::uint16_t p = 0;
    if(ul <= UINT16_MAX && end == (s.data() + s.size())) p = ul;

    return p;
}

